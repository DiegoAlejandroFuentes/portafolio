    private double[][] multiplicacion(double[][] A, double[][] B) {
        int n = this.getN();
        int m = this.getM();
        int i = this.getI();
        int j = this.getJ();
        double C[][] = new double[n][j];

        if (m == i) {
            for (int x = 0; x < n; x++) {
                for (int y = 0; y < j; y++) {
                    for (int z = 0; z < A[0].length; z++) {
                        C[x][y] += A[x][z] * B[z][y];

                    }
                }
            }
        }
        return C;
    }

    public void MatrizC(double matriz3[][], int n, int j) {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.setRowCount(n);
        model.setColumnCount(j);
        for (int x = 0; x < n; x++) {
            for (int y = 0; y < j; y++) {
                jTable3.setValueAt(matriz3[x][y], x, y);
            }
        }
    }

    private double[][] sumar(double[][] A, double[][] B) {
        int n = this.getN();
        int m = this.getM();
        int i = this.getI();
        int j = this.getJ();
        double C[][] = new double[n][j];

        if (n == i && m == j) {
            for (int x = 0; x < n; x++) {
                for (int y = 0; y < j; y++) {

                    C[x][y] = A[x][y] + B[x][y];

                }
            }
        } else {
            System.out.println("error");
        }
        return C; //To change body of generated methods, choose Tools | Templates.
    }

    private double[][] restar(double[][] A, double[][] B) {
        int n = this.getN();
        int m = this.getM();
        int i = this.getI();
        int j = this.getJ();
        double C[][] = new double[n][j];

        if (n == i && m == j) {
            for (int x = 0; x < n; x++) {
                for (int y = 0; y < j; y++) {

                    C[x][y] = A[x][y] - B[x][y];

                }
            }
        }
        return C; //To change body of generated methods, choose Tools | Templates.
    }
